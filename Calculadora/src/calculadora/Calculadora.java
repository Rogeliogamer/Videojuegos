    /*
    * Quita los puntos decimales cuando son ceros incluyendon el punto
    */
    public String sinCero(float resultado)
    {
        // Variables locales
        String retorno = "";
        retorno = Float.toString(resultado);
        
        if (resultado % 1 == 0)
        {
            retorno = retorno.substring(0, retorno.length() - 2);
            
        }
        
        return retorno;
    }
    
    private void handleOperator(JLabel Pantalla, String op)
    {
        try
        {
            numero1 = Float.parseFloat(Pantalla.getText());
            operador = op;
            Pantalla.setText("");
        }
        catch (NumberFormatException e)
        {
            Pantalla.setText("Error");
        }
        requestFocusInWindow();
    }
    
    private void handleEquals (JLabel pantalla)
    {
        try
        {
            // Variables locales
            float resultado = 0;
            numero2 = Float.parseFloat(Pantalla.getText());

            switch (operador)
            {
                case "+":
                    resultado = numero1 + numero2;
                    break;
                case "-":
                    resultado = numero1 - numero2;
                    break;
                case "*":
                    resultado = numero1 * numero2;
                    break;
                case "/":
                    if (numero2 != 0)
                    {
                        resultado = numero1 / numero2;
                    }
                    else
                    {
                        pantalla.setText("Error: No se puede dividir entre 0");
                        return;
                    }
                    break;
            }
            Pantalla.setText(formatResult(resultado));
        }
        catch (NumberFormatException e)
        {
            Pantalla.setText("Error");
        }
        requestFocusInWindow();
    }
    
    private String formatResult(float result)
    {
        DecimalFormat format = new DecimalFormat("0.#####");
        return format.format(result);
    }
    
