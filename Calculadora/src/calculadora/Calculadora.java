    private void jButton18ActionPerformed(java.awt.event.ActionEvent evt)//GEN-FIRST:event_jButton18ActionPerformed
    {//GEN-HEADEREND:event_jButton18ActionPerformed
        // Muestra el restado de las operciones en pantalla
        this.numero2 = Float.parseFloat(this.Pantalla.getText());
        
        switch(this.operador)
        {
            case "*": 
                this.Pantalla.setText(sinCero(this.numero1 * this.numero2));
                break;
            case "/":
                if (this.numero2 == 0)
                {
                    this.Pantalla.setText("Error: No se puede dividir entre 0");
                }
                else
                {
                    this.Pantalla.setText(sinCero(this.numero1 / this.numero2));
                }
                break;
            case "+": 
                this.Pantalla.setText(sinCero(this.numero1 + this.numero2));
                break;
            case "-": 
                this.Pantalla.setText(sinCero(this.numero1 - this.numero2));
                break;
        }
        requestFocusInWindow();
    }//GEN-LAST:event_jButton18ActionPerformed

